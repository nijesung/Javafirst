// **CSS
// 스타일시트라고 부르기도 하는데 HTML 에 디자인을 적용하기 위한 언어
// 1. 사용법
// 1-1. 외부에 파일을 만들고 링크를 이용해서 사용
// 1-2. html 문서 내에 style 태그를 만들고 작성해서 사용
