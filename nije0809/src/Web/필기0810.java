// 2. 작성 방법
//    선택자{속성 값;...}
//    속성과 값의 구분은 :
//    여러 개의 속성을 사용할 때는 반득시 ; 으로 구분해야 한다.

// 3. 선택자
// 3-1. *
//      모든 태그를 선택
// 3-2. 태그
//      태그는 태그 이름만 기재
// 3-3. 클래스
//      클래스 이름 앞에 . 을 추가해서 기재
// 3-4. 아이디
//      아이디 앞에 # 를 추가해서 기재
//      WebContent 디렉토리에 css 디렉토리를 만들고 temp.css 
// ex) style.html 파일을 생성

// 3-5. 속성 선택자
//      선택자[속성]: 선택자 중에 속성이 있는 경우만 적용
//      선택자[속성="값"]: 선택자 중에서 속성의 값이 일치하는 경우 = 대신에 
//                      ^= 을 사용하면 시작하는 이다.
//                      $= 는 종료하는 이다.
//                      ^= 는 포함하는 이다.
// 3-6. 가상 선택자
//      동작을 취했을 때를 나타내는 선택자 이다.
//      link 와 visited 는 a 태그에 적용하는 것으로 처음 보여질 때와 한 번이라도 누른 경우의 디자인을 변경하는 경우에 사용
//      hover 는 마우스를 올려 놓은 경우이다.
//      active 는 마우스로 클릭한 경우이다.
//      가상 선택자는 선택자 뒤에 : 를 붙이고 입력한다.
// ex) temp.css 파일에 추가
//     h3.hover{color:gray;}
//     h3:active{color:yellow;}

// 3-7. 선택자 조합
//      , : or 의 개념으로 또는 이다.
//      ex) a, p : a 와 p 태그 모두
//      공백 : 하위의 개념이다.
//      ex) p a : p 태그 안에 있는 a 태그
//      > : 자식의 개념 : 바로 아래 레벨에 존재해야 한다.
//      ex) p > a : p 태그 안에 있는 a 태그이며 다른 태그에 포함되면 안 된다.
//      + : 바로 뒤의 형제이다.
//      ~ : 모든 형제 태그이다.

